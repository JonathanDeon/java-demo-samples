package com.jonathan;

public class Capitalize {

	public char capitalize(char c) {
		switch(c) {
		case 'a':
		case 'A':
			return 'A';
		case 'b':
		case 'B':
			return 'B';
		case 'c':
		case 'C':
			return 'C';
		case 'd':
		case 'D':
			return 'D';
		case 'e':
		case 'E':
			return 'E';
		case 'f':
		case 'F':
			return 'F';
		case 'g':
		case 'G':
			return 'G';
		case 'h':
		case 'H':
			return 'H';
		case 'i':
		case 'I':
			return 'I';
		case 'j':
		case 'J':
			return 'J';
		default:
			return c;
		
			
		}
	}
}
